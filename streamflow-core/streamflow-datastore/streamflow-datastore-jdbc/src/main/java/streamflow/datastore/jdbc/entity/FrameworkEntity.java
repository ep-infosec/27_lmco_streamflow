package streamflow.datastore.jdbc.entity;

import java.io.Serializable;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;

@Entity
@NamedQueries({
    @NamedQuery(name = FrameworkEntity.FIND_ALL,
            query = "SELECT f FROM FrameworkEntity f ORDER BY f.label ASC")
})
public class FrameworkEntity implements Serializable {

    public static final String FIND_ALL = "FrameworkEntity.findAll";

    @Id
    private String id;

    private String name;

    private String label;

    @Lob
    private String entity;
    
    public FrameworkEntity() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }

    public String getEntity() {
        return entity;
    }

    public void setEntity(String entity) {
        this.entity = entity;
    }
}
